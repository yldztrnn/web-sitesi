package c04_matematikislemler_incrementdecrement;

public class c06_Pre_Post_Increment {
    public static void main(String[] args) {

        int a=10;
        //a verabile'nin değerini yazdirip,sonra a'nin değerini bir artirin

        System.out.println("a:"+ a);//10
        a++;
        //a artık 11

        int b=10;
        //a variable'nin değerini bir artirip, sonra yazdirin

        b++;
        System.out.println("b:"+b);//11
    }

}
